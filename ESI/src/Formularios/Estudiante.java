         public void mostrar()
    {
    
try {
            
            Statement Sent9=cn.createStatement();
            ResultSet rs9 = Sent9.executeQuery("SELECT E.id, E.Carne, E.Nombre, E.Fecha_nac, E.Telefono, E.Direccion,\n" +
                                                "A.Nombre FROM \n" +
                                                "estudiante E INNER JOIN \n" +
                                                "municipio A ON E.Municipio_id=A.id");
            ResultSetMetaData rsm=rs9.getMetaData();
                
            int numcolum=rsm.getColumnCount();
                 DefaultTableModel modelo = new DefaultTableModel();
                 this.tblestudiante.setModel(modelo);
                 
                 for (int x=1; x<=numcolum; x++){
                     modelo.addColumn(rsm.getColumnLabel(x));
                        
              }
                 while(rs9.next()){
                     Object [] fila = new Object[numcolum];
                     for (int y=0; y<numcolum; y++){
                         fila[y]=rs9.getObject(y+1);
                         
                     }
                     modelo.addRow(fila);
                 }
           
           
            
        }catch(Exception e){
    JOptionPane.showMessageDialog(null, e);
    
}
    }
    
    

