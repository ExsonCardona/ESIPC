    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     PreparedStatement ps;
        try {
            ps=cn.prepareCall("call instructor (?, ?, ?, ?, ?, ?)");
            ps.setString (1, this.txtnom.getText());
            ps.setString (2, this.txtap.getText());
            ps.setString (3, this.txtdpi.getText());
            ps.setString (4, this.txttel.getText());
            ps.setString (5, this.txtgra.getText());
            ps.setString (6, this.txtdir.getText());
            ps.setString (7, this.txtdip.getText());
            ps.setString (8, this.txtus.getText());
            ps.setString (9, this.txtcon.getText());
            ps.setString (10, this.txtid.getText());
         
            int res=ps.executeUpdate();
            if (res==1){
                JOptionPane.showMessageDialog (rootPane, "Datos Guardados..");
            }
        }catch (SQLException | HeadlessException e){
        }
    this.limpiarEntradas();
    }//GEN-LAST:event_jButton1ActionPerformed
